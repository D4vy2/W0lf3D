#include <stdlib.h>

char *ft_strstr(const char *s1, const char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!s2[j])
		return ((char*)&s1[i]);
	while (s1[i])
	{
		while (s1[i] == s2[j])
		{
			i++;
			j++;
			if (s2[j] == '\0')
				return ((char*)&s1[i] - j);
		}
		i = i - j;
		j = 0;
		i++;
	}
	return (NULL);
}
